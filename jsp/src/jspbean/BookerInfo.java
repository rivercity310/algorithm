package jspbean;

public class BookerInfo {
    String bookerName;
    String bookerColor;
    String bookerDateTime;

    public void setBookerName(String s) {
        this.bookerName = s;
    }

    public String getBookerName() {
        return this.bookerName;
    }

    public void setBookerColor(String s) {
        this.bookerColor = s;
    }

    public String getBookerColor() {
        return this.bookerColor;
    }

    public void setBookerDateTime(String s) {
        this.bookerDateTime = s;
    }

    public String getBookerDateTime() {
        return this.bookerDateTime;
    }
}
